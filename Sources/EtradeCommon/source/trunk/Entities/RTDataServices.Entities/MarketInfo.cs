namespace RTDataServices.Entities
{
    public class MarketInfo
    {
        public virtual System.Int32 MarketInfoId { get; set; }
        public virtual System.Int16 MarketId { get; set; }
        public virtual System.DateTime TradeDate { get; set; }
        public virtual System.Double IndexChanged { get; set; }
        public virtual System.Double SetIndex { get; set; }
        public virtual System.Int32 TotalTrade { get; set; }
        public virtual System.Int64 TotalShares { get; set; }
        public virtual System.Int64 TotalValues { get; set; }
        public virtual System.Int64 UpVolume { get; set; }
        public virtual System.Int64 NoChangeVolume { get; set; }
        public virtual System.Int64 DownVolume { get; set; }
        public virtual System.Int16 Advances { get; set; }
        public virtual System.Int16 NoChange { get; set; }
        public virtual System.Int16 Declines { get; set; }
        public virtual System.Int32 Time { get; set; }
        public virtual System.Char Status { get; set; }
        public virtual System.Int32 CountCeiling { get; set; }
        public virtual System.Int32 CountFloor { get; set; }
        public virtual System.Int32 CountNoChange { get; set; }
        public virtual IndexInfo VN30 { get; set; }

        public MarketInfo()
        {}

        public MarketInfo(System.Int32 marketInfoId, System.Int16 marketId, System.DateTime tradeDate, System.Double indexChanged, 
                          System.Double setIndex, System.Int32 totalTrade, System.Int64 totalShares, System.Int64 totalValues,
                          System.Int64 upVolume, System.Int64 noChangeVolume, System.Int64 downVolume,
                          System.Int16 advances, System.Int16 noChange, System.Int16 declines,
                          System.Int32 time, System.Char status, System.Int32 countCeiling, System.Int32 countFloor)
        {
            this.MarketInfoId   = marketInfoId;
            this.MarketId       = marketId;
            this.TradeDate      = tradeDate;
            this.IndexChanged   = indexChanged;
            this.SetIndex       = setIndex;
            this.TotalTrade     = totalTrade;
            this.TotalShares    = totalShares;
            this.TotalValues    = totalValues;
            this.UpVolume       = upVolume;
            this.NoChangeVolume = noChangeVolume;
            this.DownVolume     = downVolume;
            this.Advances       = advances;
            this.NoChange       = noChange;
            this.Declines       = declines;
            this.Time           = time;
            this.Status         = status;
            this.CountCeiling   = countCeiling;
            this.CountFloor     = countFloor;
        }
    }
}