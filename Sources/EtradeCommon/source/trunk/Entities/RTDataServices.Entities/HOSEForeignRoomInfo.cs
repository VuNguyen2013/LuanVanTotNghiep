namespace RTDataServices.Entities
{
    public class HOSEForeignRoomInfo
    {

        public virtual System.Int16 InfoID { get; set; }
        public virtual System.Int16 StockNo { get; set; }
        public virtual System.DateTime TradeDate { get; set; }
        public virtual System.Int64 TotalRoom { get; set; }
        public virtual System.Int64 CurrentRoom { get; set; }

        public HOSEForeignRoomInfo()
        {}

        public HOSEForeignRoomInfo(System.Int16 InfoID, System.Int16 StockNo, System.DateTime TradeDate, System.Int64 TotalRoom, System.Int64 CurrentRoom)
        {
            this.InfoID = InfoID;
            this.StockNo     = StockNo;
            this.TradeDate   = TradeDate;
            this.TotalRoom   = TotalRoom;
            this.CurrentRoom = CurrentRoom;
        }
    }
}