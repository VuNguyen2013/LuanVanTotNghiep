using System;
namespace ETradeCore.Entities
{
    public class MarginPortfolio
    {
        /// <summary>
        /// Gets or sets the account no.
        /// </summary>
        /// <value>The account no from SBA.</value>
        public string accountNo { get; set; }

        /// <summary>
        /// Gets or sets the stock (symbol).
        /// </summary>
        /// <value>The stock.</value>
        public string Stock { get; set; }

        /// <summary>
        /// Gets or sets the Grade.
        /// </summary>
        /// <value>The Grade.</value>
        public string G { get; set; }

        /// <summary>
        /// Gets or sets the stock type.
        /// </summary>
        /// <value>The the stock type.</value>
        public string TY { get; set; }

        /// <summary>
        /// Gets or sets the actual_ vol.
        /// </summary>
        /// <value>The actual_ vol.</value>
        public Decimal Actual_Vol { get; set; }

        /// <summary>
        /// Gets or sets the AV g_ cost.
        /// </summary>
        /// <value>The AV g_ cost.</value>
        public Decimal AVG_Cost { get; set; }

        /// <summary>
        /// Gets or sets the amount.
        /// </summary>
        /// <value>The amount.</value>
        public Decimal Amount { get; set; }

        /// <summary>
        /// Gets or sets the last sell.
        /// </summary>
        /// <value>The last sell.</value>
        public Decimal Last { get; set; }

        /// <summary>
        /// Gets or sets the MK t_ value.
        /// </summary>
        /// <value>The MK t_ value.</value>
        public Decimal MKT_Value { get; set; }

        /// <summary>
        /// Gets or sets the MR.
        /// </summary>
        /// <value>The MR.</value>
        public Decimal MR { get; set; }

        /// <summary>
        /// Gets or sets the rate.
        /// </summary>
        /// <value>The rate.</value>
        public Decimal Rate { get; set; }
    }
}