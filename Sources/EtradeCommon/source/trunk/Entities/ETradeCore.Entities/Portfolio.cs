namespace ETradeCore.Entities
{
    public class Portfolio
    {
        /// <summary>
        /// Gets or sets the symbol.
        /// </summary>
        /// <value>The symbol.</value>
        public System.String Symbol { get; set; }

        /// <summary>
        /// Gets or sets the type of the sec.
        /// </summary>
        /// <value>The type of the sec.</value>
        public System.Int32 SecType { get; set; }

        /// <summary>
        /// Gets or sets the avg price.
        /// </summary>
        /// <value>The avg price.</value>
        public System.Decimal AvgPrice { get; set; }

        /// <summary>
        /// Gets or sets the start price.
        /// </summary>
        /// <value>The start price.</value>
        public System.Decimal StartPrice { get; set; }

        /// <summary>
        /// Gets or sets the available.
        /// </summary>
        /// <value>The available.</value>
        public System.Decimal Available { get; set; }

        /// <summary>
        /// Gets or sets the MKT value.
        /// </summary>
        /// <value>The MKT value.</value>
        public System.Decimal MktValue { get; set; }

        /// <summary>
        /// Gets or sets the last sale.
        /// </summary>
        /// <value>The last sale.</value>
        public System.Decimal LastSale { get; set; }

        /// <summary>
        /// Gets or sets the Amount
        /// </summary>
        /// <value>The Amount</value>
        public System.Decimal Amount { get; set; }

        public System.Decimal Total { get; set; }
    }
}