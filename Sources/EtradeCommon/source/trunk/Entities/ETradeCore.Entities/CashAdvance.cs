namespace ETradeCore.Entities
{
    public class CashAdvance
    {
        /// <summary>
        /// Gets or sets the customer no.
        /// </summary>
        /// <value>The customer no.</value>
        public System.String CustomerNo { get; set; }

        /// <summary>
        /// Gets or sets the name of the customer.
        /// </summary>
        /// <value>The name of the customer.</value>
        public System.String CustomerName { get; set; }

        /// <summary>
        /// Gets or sets the trade date.
        /// </summary>
        /// <value>The trade date.</value>
        public System.DateTime TradeDate { get; set; }

        /// <summary>
        /// Gets or sets the sell value.
        /// </summary>
        /// <value>The sell value.</value>
        public System.Decimal SellValue { get; set; }

        /// <summary>
        /// Gets or sets the sell amt.
        /// </summary>
        /// <value>The sell amt.</value>
        public System.Decimal SellAmt { get; set; }

        /// <summary>
        /// Gets or sets the trade commission.
        /// </summary>
        /// <value>The trade commission.</value>
        public System.Decimal TradeCommission { get; set; }

        /// <summary>
        /// Gets or sets the contract no.
        /// </summary>
        /// <value>The contract no.</value>
        public System.String ContractNo { get; set; }

        /// <summary>
        /// Gets or sets the gross with draw.
        /// </summary>
        /// <value>The gross with draw.</value>
        public System.Decimal GrossWithDraw { get; set; }

        /// <summary>
        /// Gets or sets the net with draw.
        /// </summary>
        /// <value>The net with draw.</value>
        public System.Decimal NetWithDraw { get; set; }

        /// <summary>
        /// Gets or sets the fee.
        /// </summary>
        /// <value>The fee.</value>
        public System.Decimal AdvFee { get; set; }

        /// <summary>
        /// Gets or sets the vat.
        /// </summary>
        /// <value>The vat.</value>
        public System.Decimal AdvVat { get; set; }

        /// <summary>
        /// Gets or sets the user id.
        /// </summary>
        /// <value>The user id.</value>
        public System.String UserId { get; set; }

        /// <summary>
        /// Gets or sets the confirm user.
        /// </summary>
        /// <value>The confirm user.</value>
        public System.String ConfirmUser { get; set; }

        /// <summary>
        /// Gets or sets the edit date.It is also the Cash Advance day.
        /// </summary>
        /// <value>The edit date.</value>
        public System.DateTime EditDate { get; set; }

        /// <summary>
        /// Gets or sets the edit time.
        /// </summary>
        /// <value>The edit time.</value>
        public System.DateTime EditTime { get; set; }

        /// <summary>
        /// Gets or sets the confirm time.
        /// </summary>
        /// <value>The confirm time.</value>
        public System.DateTime ConfirmTime { get; set; }

        /// <summary>
        /// Gets or sets the pledge value.
        /// </summary>
        /// <value>The pledge value.</value>
        public System.Decimal PledgeValue { get; set; }

        /// <summary>
        /// Gets or sets the pledge amt.
        /// </summary>
        /// <value>The pledge amt.</value>
        public System.Decimal PledgeAmt { get; set; }

        /// <summary>
        /// Gets or sets the loan amt.
        /// </summary>
        /// <value>The loan amt.</value>
        public System.Decimal LoanAmt { get; set; }

        /// <summary>
        /// Gets or sets the remark.
        /// </summary>
        /// <value>The remark.</value>
        public System.String Remark { get; set; }

        /// <summary>
        /// the status of advance order.
        /// </summary>
        public System.Int16 Status { get; set; }

        /// <summary>
        /// Gets or sets the broker id.
        /// </summary>
        /// <value>The broker id.</value>
        public System.String BrokerId { get; set; }

        /// <summary>
        /// Gets or sets the due date.
        /// </summary>
        /// <value>The due date.</value>
        public System.DateTime DueDate { get; set; }

        /// <summary>
        /// Gets or sets the account no2.
        /// </summary>
        /// <value>The account no2.</value>
        public System.String AccountNo2 { get; set; }
    }
}