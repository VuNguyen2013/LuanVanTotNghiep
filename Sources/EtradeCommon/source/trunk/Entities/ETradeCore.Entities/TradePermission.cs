namespace ETradeCore.Entities
{
    public class TradePermission
    {
        /// <summary>
        /// Gets or sets a value indicating whether this instance can buy.
        /// </summary>
        /// <value><c>true</c> if this instance can buy; otherwise, <c>false</c>.</value>
        public System.Boolean CanBuy { get; set; }

        /// <summary>
        /// Gets or sets a value indicating whether this instance can sell.
        /// </summary>
        /// <value><c>true</c> if this instance can sell; otherwise, <c>false</c>.</value>
        public System.Boolean CanSell { get; set; }

        /// <summary>
        /// Gets or sets a value indicating whether this instance is lock.
        /// This status is get from core
        /// </summary>
        /// <value><c>true</c> if this instance is lock; otherwise, <c>false</c>.</value>
        public System.Boolean IsLock { get; set; }

        /// <summary>
        /// Gets or sets a value indicating whether this instance is active.
        /// This status is get from OTS database
        /// </summary>
        /// <value><c>true</c> if this instance is active; otherwise, <c>false</c>.</value>
        public System.Boolean IsActive { get; set; }
    }
}