#region Using directives

using System;

#endregion

namespace ETradeFinance.Entities
{

    public class StockTransferInfo
    {
        /// <summary>
        /// Gets or sets the sub Account Id
        /// </summary>
        /// <value>The sub Account Id.</value>
        public String subAccountId { get; set; }

        /// <summary>
        /// Gets or sets the sec symbol.
        /// </summary>
        /// <value>The sec symbol.</value>
        public String SecSymbol { get; set; }

        /// <summary>
        /// Gets or sets the withdrawable amt.
        /// </summary>
        /// <value>The withdrawable amt.</value>
        public long WithdrawableAmt { get; set; }

        /// <summary>
        /// Gets or sets the transfered amt.
        /// </summary>
        /// <value>The transfered amt.</value>
        public long TransferedAmt { get; set; }

        /// <summary>
        /// Gets or sets the adv order amt.
        /// </summary>
        /// <value>The adv order amt.</value>
        public long AdvOrderAmt { get; set; }

        /// <summary>
        /// Gets or sets the avilable amt.
        /// </summary>
        /// <value>The avilable amt.</value>
        public long AvilableAmt { get; set; }
    }
	
}
